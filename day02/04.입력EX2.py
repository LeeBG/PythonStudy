"""
(조건)
1. 값을 3개 입력을 받아 변수에 저장합니다.
2. 변수명은 각각 value1,value2,value3 으로 합니다.
3. 저장된 값을 이용해 결과처럼 출력하세요.
4. 입력한 갑과 연동되어야 합니다.
5. 값을 저장한 변수는 최대한 적게 사용해보세요.
"""

# 변수는 준비하는데 절대로 제한이 없음
print("--입력구간--")
value1 = input("값1 >>")
value2 = input("값2 >>")
value3 = input("값3 >>")

# 준비한 변수가 필요한가?는 나중에 결정됨
print("--출력구간--")
print(value1,value3,value2)
print(value1,value1,value3)
print(value3,value2,value1)

# ★변수가 무조건 준비가 되는 상황이면 최대한 활용하도록 만든다.
