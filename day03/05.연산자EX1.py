value1 = 100
value2 = 3.14

print(value1,"+",value2,"=",value1+value2) # 덧셈
print(value1,"-",value2,"=",value1-value2) # 뺄셈
print(value1,"x",value2,"=",value1*value2) # 곱하기
print(value1,"/",value2,"=",value1/value2) # 나누기
print(value1,"%",value2,"=",value1%value2) # 나머지(정수)
print(value1,"//",value2,"=",value1//value2) # 몫(정수)
print(value1,"**",value2,"=",value1**value2) # 거듭제곱

